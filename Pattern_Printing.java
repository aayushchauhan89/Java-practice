     /* Pascal's Trianlge          
     1 
     1 1 
    1 2 1 
   1 3 3 1 
  1 4 6 4 1   */
        public static void main(String[] args) {
          Scanner sc = new Scanner(System.in);
          System.out.print("Enter number of rows: ");
          int rows = sc.nextInt();
  
          for (int i = 1; i <= rows; i++) {
              int number = 1;
  
             
              for (int j = 1; j <= rows - i; j++) {
                  System.out.print("  ");
              }
  
             
              for (int j = 1; j <= i; j++) {
                  System.out.print(number + "   ");
                  number = number * (i - j) / j;
              }
  
              System.out.println();
          }
  
          sc.close();
      }
